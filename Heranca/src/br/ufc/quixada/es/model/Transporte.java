package br.ufc.quixada.es.model;

public class Transporte {
	
	private int capacidade;
	
	public Transporte() {
		super();
	}
	
	public Transporte(int capacidade) {
		super();
		this.capacidade = capacidade;
	}
	
	public int getCapacidade() {
		return capacidade;
	}

	public void setCapacidade(int capacidade) {
		this.capacidade = capacidade;
	}
	
	

}
